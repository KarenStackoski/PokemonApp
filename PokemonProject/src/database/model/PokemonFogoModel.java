package database.model;

public class PokemonFogoModel {
	private int id;
	private String pokemonFogo;
	
	public PokemonFogoModel() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getPokemonFogo() {
		return pokemonFogo;
	}

	public void setPokemonFogo(String pokemonFogo) {
		this.pokemonFogo = pokemonFogo;
	}
}

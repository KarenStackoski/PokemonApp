package database.model;

public class PokemonEletricoModel {
	private int id;
	private String pokemonEletrico;
	
	public PokemonEletricoModel() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getPokemonEletrico() {
		return pokemonEletrico;
	}

	public void setPokemonEletrico(String pokemonEletrico) {
		this.pokemonEletrico = pokemonEletrico;
	}
}
